package design.patterns.design.patterns.calculadora

class Calculadora {



    fun sumar(a: Int, b: Int) : Int{
        return a + b
    }

    fun restar(a: Int, b: Int) : Int{
        return a - b
    }

    fun dividir(a: Int, b: Int) : Int{
        return a / b
    }

    fun multiplicar(a: Int, b: Int) : Int{
        return a * b
    }
}